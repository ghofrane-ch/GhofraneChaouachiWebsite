let currentLanguage = 'en';
function setLanguage(language) {
    currentLanguage = language;
    document.querySelectorAll('[data-translate]').forEach((element) => {
        const key = element.getAttribute('data-translate');
        if (translations[language][key]) {
            element.textContent = translations[language][key];
        }
    });
}

function toggleTasks(button) {
    const tasksContainer = button.nextElementSibling;
    if (tasksContainer.style.display === 'block') {
        tasksContainer.style.display = 'none';
        button.innerText = translations[currentLanguage]["showTasks"];
    } else {
        tasksContainer.style.display = 'block';
        button.innerText = translations[currentLanguage]["hideTasks"];
    }
}

